-- ─── 4. Row Level Security (RLS) ─────────────────────────────────────────────
-- All tenant-scoped tables enforce company isolation at the DB level.
-- The NestJS API sets these via: SET LOCAL app.current_company = '...'
-- Even if the application has a bug, data from other companies is NEVER returned.

-- Enable RLS on all tenant-scoped tables
DO $$
DECLARE
  tbl TEXT;
  tenant_tables TEXT[] := ARRAY[
    'users', 'roles', 'user_roles',
    'chart_of_accounts', 'accounting_periods', 'journal_entries', 'journal_entry_lines',
    'posting_profiles', 'cost_centers', 'exchange_rates',
    'product_categories', 'product_templates', 'product_variants', 'variant_barcodes',
    'price_lists', 'price_list_items',
    'warehouses', 'inventory_balances', 'stock_ledger_entries',
    'stock_transfers', 'stock_transfer_lines',
    'stocktaking_sessions', 'stocktaking_lines',
    'reorder_points',
    'audit_logs',
    'document_sequences',
    'system_policies'
  ];
BEGIN
  FOREACH tbl IN ARRAY tenant_tables LOOP
    BEGIN
      EXECUTE format('ALTER TABLE %I ENABLE ROW LEVEL SECURITY', tbl);
      EXECUTE format('ALTER TABLE %I FORCE ROW LEVEL SECURITY', tbl);
    EXCEPTION WHEN undefined_table THEN
      -- Table will be created by Prisma migration; RLS applied after
      NULL;
    END;
  END LOOP;
END $$;

-- RLS Policies: company isolation
-- Pattern: users can only see rows where company_id = current_setting('app.current_company')
-- Super Admin bypass: set app.current_company = '*' to see all companies

CREATE OR REPLACE FUNCTION current_company_id() RETURNS UUID AS $$
  SELECT NULLIF(current_setting('app.current_company', true), '')::UUID;
$$ LANGUAGE SQL STABLE;

-- ─── 5. Append-Only Triggers ─────────────────────────────────────────────────
-- AuditLog and StockLedgerEntry are NEVER updated or deleted.
-- These triggers enforce immutability at the database level.

CREATE OR REPLACE FUNCTION prevent_update_delete()
RETURNS TRIGGER AS $$
BEGIN
  RAISE EXCEPTION 'Table % is append-only. Updates and deletes are not permitted.',
    TG_TABLE_NAME
    USING ERRCODE = 'restrict_violation';
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

-- Applied to audit_logs (immutable audit trail)
DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'audit_logs') THEN
    DROP TRIGGER IF EXISTS no_update_audit_logs ON audit_logs;
    CREATE TRIGGER no_update_audit_logs
      BEFORE UPDATE OR DELETE ON audit_logs
      FOR EACH ROW EXECUTE FUNCTION prevent_update_delete();
  END IF;
END $$;

