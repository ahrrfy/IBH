-- ─── 6. Double-Entry Balance Check ───────────────────────────────────────────
-- Enforces that every JournalEntry has totalDebitIqd = totalCreditIqd.
-- DB-level guarantee: no unbalanced entry can ever be saved.

DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'journal_entries') THEN
    ALTER TABLE journal_entries
      DROP CONSTRAINT IF EXISTS balanced_entry;
    ALTER TABLE journal_entries
      ADD CONSTRAINT balanced_entry
      CHECK (total_debit_iqd = total_credit_iqd);
  END IF;
END $$;

-- ─── 7. Period Lock Trigger ───────────────────────────────────────────────────
-- Prevents any INSERT into journal_entries for a closed accounting period.

CREATE OR REPLACE FUNCTION check_period_open()
RETURNS TRIGGER AS $$
DECLARE
  period_status TEXT;
BEGIN
  SELECT status INTO period_status
  FROM accounting_periods
  WHERE id = NEW.period_id;

  IF period_status != 'open' THEN
    RAISE EXCEPTION 'Cannot post to a % accounting period', period_status
      USING ERRCODE = 'check_violation',
            HINT = 'Open the period first or create a new one';
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'journal_entries') THEN
    DROP TRIGGER IF EXISTS enforce_period_lock ON journal_entries;
    CREATE TRIGGER enforce_period_lock
      BEFORE INSERT ON journal_entries
      FOR EACH ROW EXECUTE FUNCTION check_period_open();
  END IF;
END $$;

