-- ─── 1. ULID Generation Function ────────────────────────────────────────────
-- Generates a ULID (Universally Unique Lexicographically Sortable Identifier)
-- 26 characters, Crockford base32 encoding, timestamp-prefixed
-- Requires pgcrypto extension for gen_random_bytes()

CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE EXTENSION IF NOT EXISTS pg_trgm;  -- for fast text search

CREATE OR REPLACE FUNCTION gen_ulid() RETURNS TEXT AS $$
DECLARE
  -- Crockford's Base32 alphabet (no I, L, O, U to avoid confusion)
  encoding  TEXT := '0123456789ABCDEFGHJKMNPQRSTVWXYZ';
  output    TEXT := '';
  now_ms    BIGINT;
  rand_bytes BYTEA;
  i         INT;
BEGIN
  -- 10 characters from timestamp (milliseconds since epoch)
  now_ms := EXTRACT(EPOCH FROM clock_timestamp()) * 1000;
  FOR i IN REVERSE 9..0 LOOP
    output := output || SUBSTR(encoding, (now_ms >> (i * 5) & 31) + 1, 1);
  END LOOP;

  -- 16 characters from random bytes (80 bits of randomness)
  rand_bytes := gen_random_bytes(10);
  FOR i IN 0..9 LOOP
    output := output || SUBSTR(encoding, (GET_BYTE(rand_bytes, i) >> 3 & 31) + 1, 1);
    IF i < 9 THEN
      output := output || SUBSTR(encoding, ((GET_BYTE(rand_bytes, i) & 7) << 2 | GET_BYTE(rand_bytes, i+1) >> 6) & 31 + 1, 1);
    END IF;
  END LOOP;

  RETURN SUBSTR(output, 1, 26);
END;
$$ LANGUAGE plpgsql VOLATILE;

